// Next Components
import Head from 'next/head';
import Image from 'next/image';

// Components
import Layout from '../components/Layout';
import ItemTrack from '../components/ItemTrack';

// Hooks
import { useEffect, useState } from 'react';

// Styles
import { container, tracksContainer } from '../styles/Home.module.css';
import { textMd, textWhite, textBold } from '../styles/utils.module.css';

// NextAuth
import { useSession } from 'next-auth/react';

export default function Home() {
	const [userData, setData] = useState(null);
	const [isLoading, setLoading] = useState(false);
	const { data: session, status } = useSession();
	useEffect(() => {
		setLoading(true);
		fetch('/api/getRecentlyPlayed')
			.then((res) => {
				return res.json();
			})
			.then((res) => {
				setData(res);
				setLoading(false);
			});
	}, []);
	if (isLoading) {
		return (
			<Layout>
				<div className={container}>
					<p className={`${textMd} ${textWhite} ${textBold}`}>Loading...</p>
				</div>
			</Layout>
		);
	}
	if (session) {
		return (
			<>
				<Head>
					<title>Next Spotify | Home</title>
					<meta name='description' content='Generated by create next app' />
					<link rel='icon' href='/favicon.ico' />
				</Head>

				<Layout>
					<div className={container}>
						<div className={tracksContainer}>
							<ItemTrack userData={userData}>Recently Played</ItemTrack>
						</div>
					</div>
				</Layout>
			</>
		);
	}
	return (
		<>
			<Layout>
				<div className={container}>
					<h1 className={`${textMd} ${textWhite} ${textBold}`}>
						Please Sign In
					</h1>
				</div>
			</Layout>
		</>
	);
}
